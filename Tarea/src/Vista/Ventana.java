    private void cargarTabla(){
        
       
        DefaultTableModel dm = new DefaultTableModel();
        
        dm.setColumnIdentifiers(new String[]  {"Paciente","Legajo","Nombre" , "Duracion"});
        
        Gestor g = new Gestor();
        ArrayList<Visita> lista = g.obtenerVisitas2();
        
        
        for (Visita visita : lista) {
            
               dm.addRow(new String[] 
            {
             String.valueOf(visita.getPaciente()),
             String.valueOf(visita.getEmpleado().getLegajo()),
             visita.getNombre(),
             String.valueOf(visita.getDuracion())});
            
        }
       
        lstTabla.setModel(dm);
        
    }
    private void cargarCombo() {

        Gestor gestor = new Gestor();

        ArrayList<Paciente> lista = gestor.ComboPaciente();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        
        for (Paciente paciente : lista) {
            modelo.addElement(paciente);
        }

        cboPaciente.setModel(modelo);
}
