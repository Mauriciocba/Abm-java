    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if(!txtNombre.getText().equals("") && !txtDuracion.getText().equals("")) {
                
        Gestor gestor = new Gestor();
        
        Paciente pa =(Paciente) cboPaciente.getSelectedItem();
        Empleado em =(Empleado) cboLegajo.getSelectedItem();
        String nombre = txtNombre.getText();
        int duracion = Integer.parseInt(txtDuracion.getText());

        Visita vi = new Visita (0,pa,em,nombre,duracion);
        gestor.agregarUnaVisita(vi);
        cargarTabla(); 
        }
        else 
        {   
                JOptionPane.showMessageDialog(this, "Hay campos vacios");
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

